package day03_VariablesContinue;

public class ArithmeticOperators {
    public static void main(String[] args) {
        //+,-<*,/,%

        System.out.println(5+3); // 8, addition
        System.out.println(10-3); // 7, subtraction
        System.out.println(10*2); // 10, multiplication
        System.out.println(10/2); // 5, division
        System.out.println(10%3); // 1, remainder








    }





}
