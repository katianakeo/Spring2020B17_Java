package day02_variables;

public class EscapeSequences {
    public static void main(String[] args) {

        System.out.println("Hello World");
        System.out.println("\tHello World");
        System.out.println("one two \nthree");
        System.out.println("\n\n\n Hello");
        // My favorite TV show is "game of thrones"
        System.out.println("my favorite TV show is \"game of thrones\"");
        System.out.println("she said \" hi \" ");
        System.out.println("Hello my name is \n katiana");
        System.out.println("hello \\ hi");








    }


}
