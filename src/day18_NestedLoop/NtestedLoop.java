package day18_NestedLoop;

public class NtestedLoop {
    public static void main(String[] args) {
        for(int j = 0; j<10; j++){
            for(int i =1; i<=5; i++){
                System.out.print(i);

            }
            System.out.println();
        }



        for(int j =0; j<7; j++){
            int a = 1;
            while(a<=7){
                System.out.print("*");
                a++;
            }
            System.out.println();
        }


    }
}
