package day02_variables;

public class StringOfText {
    public static void main(String[] args) {
        String employeeName = "katiana";

        System.out.println(employeeName); // Muhtar

        String companyName = "P.F. Changs";
        System.out.println(companyName); // Bank of America

        /*
        Employee Name is: Muhtar
        Company Name is: Bank of America
        Muhtar works at Bank of America
         */

        System.out.println("Employee name is: " +employeeName);
        System.out.println("Company name is: " +companyName);
        System.out.println(employeeName+ " works at " +companyName);

        String schoolName = "cybertek";
        System.out.println(schoolName);

        String studentName = "Katiana";
        System.out.println(studentName);

        System.out.println(studentName+" goes to "+schoolName);
        System.out.println(studentName+"\n"+schoolName);






    }





}
