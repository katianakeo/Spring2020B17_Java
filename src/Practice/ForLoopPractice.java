package Practice;

public class ForLoopPractice {
    public static void main(String[] args) {
        for(int i=0; i<=5; i++){  // i++ = 1,2,3,4,5
                                  // i+=2 = 0,2,4
            System.out.println("Hello World " + i);
        }

        for(int i=1; i<=5; i++){
            System.out.print(i);
        }


    }
}
